using System;

public class NumbersPractice
{
	public NumbersPractice()
	{
		// Explore integer math
		int a = 18;
		int b = 6;
		int c = a + b;
		Console.WriteLine("18 + 6 = " + c);
		c = a - b;
		Console.WriteLine("18 - 6 = " + c);
		c = a * b;
		Console.WriteLine("18 * 6 = " + c);
		c = a / b;
		Console.WriteLine("18 / 6 = " + c);
	}
}
